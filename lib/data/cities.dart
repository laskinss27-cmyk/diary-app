/// Поиск городов по префиксу, без учёта регистра.
/// Нормализует "ё → е" для удобства ввода (пользователь может не
/// переключаться на раскладку с ё).
List<City> searchCities(String query, {int limit = 8}) {
  final q = _normalize(query);
  if (q.isEmpty) return const [];
  final result = <City>[];
  for (final c in kCities) {
    if (_normalize(c.name).startsWith(q)) {
      result.add(c);
      if (result.length >= limit) break;
    }
  }
  return result;
}

/// Точный поиск по полному названию (для восстановления City из
/// сохранённой строки профиля).
City? findCityByName(String name) {
  final n = _normalize(name);
  for (final c in kCities) {
    if (_normalize(c.name) == n) return c;
  }
  return null;
}

String _normalize(String s) => s.toLowerCase().replaceAll('ё', 'е').trim();
